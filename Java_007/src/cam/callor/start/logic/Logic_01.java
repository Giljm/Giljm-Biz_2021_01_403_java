package cam.callor.start.logic;

public class Logic_01 {

	/*
	 * 논리(logic) 연산
	 * 참(yes, true), 거짓(no, fales)을 판단하여 어떤 행위를 수행하는 연산
	 */
	
	public static void main(String[] args) {
		
		// 3이 3 미만이냐(보다 작냐)
		System.out.println(3 < 3);
		System.out.println(3 > 3);
		
		// 3이냐 3이냐
		System.out.println(3 == 3);
		
		// 3이 3이하 (보다 작거나 같냐)
		System.out.println(3 <= 3);
		
		// 3이 3이상 (보다 크거나 같냐)
		System.out.println( 3 >= 3);
	}
	
}
